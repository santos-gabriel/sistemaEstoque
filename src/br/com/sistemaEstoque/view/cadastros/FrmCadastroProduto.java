    private void desabilitarCampos() {
        btnExcluir.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnEditar.setEnabled(false);
        txtCodigo.setText("");
        txtNome.setText("");
        txtNome.setEditable(false);
        txtPreco.setText("");
        txtPreco.setEditable(false);
        txtQuantidade.setText("");
        txtQuantidade.setEditable(false);
    }

    private void habilitarCampos() {
        txtCodigo.setText("");
        txtNome.setText("");
        txtNome.setEditable(true);
        txtNome.requestFocus();
        txtPreco.setText("");
        txtPreco.setEditable(true);
        txtQuantidade.setText("");
        txtQuantidade.setEditable(true);
    }

    private void habilitarCamposSemFocus() {
        txtCodigo.setText("");
        txtNome.setText("");
        txtNome.setEditable(true);
        txtPreco.setText("");
        txtPreco.setEditable(true);
        txtQuantidade.setText("");
        txtQuantidade.setEditable(true);
    }

    private void ordenarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tbProduto.getModel();
        tbProduto.setRowSorter(new TableRowSorter(modelo));
    }

    private void gravarBD() {
        Produto prod = new Produto();
        prod.setNomeProduto(txtNome.getText());
        prod.setPrecoProduto(Double.parseDouble(txtPreco.getText().replaceAll(",", ".")));
        prod.setQuantidadeProduto(Integer.parseInt(txtQuantidade.getText()));
        CtrlProduto.inserir(prod);
    }

    private void listarTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tbProduto.getModel();
        dtm.setNumRows(0);

        for (Produto prod : CtrlProduto.list()) {

            dtm.addRow(new Object[]{
                prod.getIdProduto(),
                prod.getNomeProduto(),
                prod.getPrecoProduto(),
                prod.getQuantidadeProduto()
            });

        }
    }

