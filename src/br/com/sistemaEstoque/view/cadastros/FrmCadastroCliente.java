    private void ordenarTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tbCliente.getModel();
        tbCliente.setRowSorter(new TableRowSorter(modelo));
    }

    private void habilitarCampos() {
        txtNome.setText("");
        txtNome.setEditable(true);
        txtNome.requestFocus();
    }

    private void desabilitarCampos() {
        btnExcluir.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnEditar.setEnabled(false);
        txtNome.setText("");
        txtNome.setEditable(false);
    }

    private void gravarBD() {
        Cliente cli = new Cliente();
        cli.setNomeCliente(txtNome.getText());
        ClienteDAO cliDAO = new ClienteDAO();
        cliDAO.salvar(cli);
    }

    private void listarTabela() {
        DefaultTableModel dtm = (DefaultTableModel) tbCliente.getModel();
        dtm.setNumRows(0);
        ClienteDAO clienteDAO = new ClienteDAO();

        for (Cliente cli : clienteDAO.leitura()) {

            dtm.addRow(new Object[]{
                cli.getIdCliente(),
                cli.getNomeCliente()
            });

        }

    }

