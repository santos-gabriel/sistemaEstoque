    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (txtNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome do cliente");
            txtNome.requestFocus(true);
        } else {
            gravarBD();
            DefaultTableModel dtm = (DefaultTableModel) tbCliente.getModel();
            Object[] conteudo = {txtCodigo.getText(), txtNome.getText()};
            dtm.addRow(conteudo);
            txtNome.setText("");
            txtNome.setEditable(false);
            desabilitarCampos();
            listarTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tbCliente.getModel();
        if (tbCliente.getSelectedRow() != -1) {
            Cliente cliente = new Cliente();
            ClienteDAO clienteDAO = new ClienteDAO();

            cliente.setIdCliente((int) tbCliente.getValueAt(tbCliente.getSelectedRow(), 0));

            clienteDAO.delete(cliente);
            desabilitarCampos();
            listarTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente a ser excluído");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void tbClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbClienteMouseClicked
        if (tbCliente.getSelectedRow() != -1) {
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
            txtNome.setEditable(true);
            txtCodigo.setText(tbCliente.getValueAt(tbCliente.getSelectedRow(), 0).toString());
            txtNome.setText(tbCliente.getValueAt(tbCliente.getSelectedRow(), 1).toString());
        }
    }//GEN-LAST:event_tbClienteMouseClicked

    private void tbClienteKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbClienteKeyReleased
        if (tbCliente.getSelectedRow() != -1) {
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
            txtNome.setEditable(true);
            txtCodigo.setText(tbCliente.getValueAt(tbCliente.getSelectedRow(), 0).toString());
            txtNome.setText(tbCliente.getValueAt(tbCliente.getSelectedRow(), 1).toString());
        }
    }//GEN-LAST:event_tbClienteKeyReleased

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tbCliente.getSelectedRow() != -1) {
            Cliente cliente = new Cliente();
            ClienteDAO clienteDAO = new ClienteDAO();

            cliente.setIdCliente((int) tbCliente.getValueAt(tbCliente.getSelectedRow(), 0));
            cliente.setNomeCliente(txtNome.getText());

            clienteDAO.editar(cliente);
            desabilitarCampos();
            listarTabela();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

